    private void btn_cargarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_cargarMouseClicked
        
        JFileChooser filechooser = new JFileChooser();
        int resultado = filechooser.showOpenDialog(this);
        
        
        if(resultado == JFileChooser.APPROVE_OPTION){ //tenemos un archivo para trabajar
            File seleccionado = filechooser.getSelectedFile();
            try{
                /* NO PODEMOS CARGAR TODO EL ARCHIVO DE UNA VEZ*/
                // BUFFER
                BufferedReader br = new BufferedReader(new FileReader(seleccionado));
                
                String linea = br.readLine();
                
                while(linea!=null){
                    txt_contenido.append(linea + "\n");
                    // CSV -> nombre,apellido,identidad
//                    // nombre, apellido, id, [clase1;clase2;clase3]
//                    "claudia,cortes,123"
                    String[] valores = linea.split(",");
                    linea = br.readLine();
                }
                
                br.close();
                
                
            }catch(IOException ex){
                
            }
            
        }else{
            System.out.println("algo salio mal o cancelamos la acci√≥n");
        }
    }//GEN-LAST:event_btn_cargarMouseClicked

    private void btn_guardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_guardarMouseClicked
         JFileChooser filechooser = new JFileChooser();
        int resultado = filechooser.showSaveDialog(this);
        
        
        if(resultado == JFileChooser.APPROVE_OPTION){ //tenemos un archivo para trabajar
            /*
            EXISTEN DOS FORMAS DE ESCRIBIR EN UN ARCHIVO: 
            1) append:Mantener el contenido original del archivo y agregar nuevo contenido a lo que ya tenemos
            2)override:  Borramos el contenido anterior dle archivo y lo reemplazamos por el nuevo contenido
            */
            File seleccionado = filechooser.getSelectedFile();
            Boolean append = true;
            try{
                BufferedWriter br = new BufferedWriter(new FileWriter(seleccionado,append ));
                br.write(txt_contenido.getText());
                br.close();
                JOptionPane.showMessageDialog(this, "AARCHIVO SALVADO EXITOSAMENTE!");
            }catch(IOException ex){
                
            }
        }
    }//GEN-LAST:event_btn_guardarMouseClicked

